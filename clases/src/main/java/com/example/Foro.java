package com.example;

public class Foro {
    private String id;
    private String tematica;
    private String numeroDeComentarios;

    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getTematica() {
        return tematica;
    }
    public void setTematica(String tematica) {
        this.tematica = tematica;
    }
    public String getNumeroDeComentarios() {
        return numeroDeComentarios;
    }
    public void setNumeroDeComentarios(String numeroDeComentarios) {
        this.numeroDeComentarios = numeroDeComentarios;
    }
    
}
